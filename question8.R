# 1. Numeric
num_var <- 10.5
print(num_var)        
print(class(num_var)) 

# 2. Integer
int_var <- 25L
print(int_var)        
print(class(int_var)) 

# 3. Character
char_var <- "Deepthi"
print(char_var)       
print(class(char_var))

# 4. Logical
log_var <- TRUE
print(log_var)        
print(class(log_var))

# 5. Complex
comp_var <- 3 + 4i
print(comp_var)       
print(class(comp_var))
